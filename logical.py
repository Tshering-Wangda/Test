# AND Operator(Both conditions should be true)
x = 5
print(x > 3 and x < 10)

y = 12
print(y > 10 and y % 5 == 0)

# OR Operator(True if one of the condition is true)
x = 5
print(x < 3 or x > 10)

y = 12
print(y < 10 or y % 2 == 0)

# NOT Operator(Reverse)
x = 5
print(not(x > 3 and x < 10))

y = 12
print(not(y > 10 and y % 5 == 0))